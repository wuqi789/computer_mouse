#ifndef __MOUSE_CONFIG_H
#define __MOUSE_CONFIG_H


/*********************************************************************************************************
  常量宏定义 -- 迷宫类型
*********************************************************************************************************/
#define MAZETYPE        16                                              /*  8: 四分之一迷宫；16: 全迷宫 */


/*********************************************************************************************************
  常量宏定义 -- 设定四分之一迷宫的终点坐标
*********************************************************************************************************/
#if MAZETYPE == 8
#define XDST0           7
#define XDST1           8
#define YDST0           7
#define YDST1           8
#endif


/*********************************************************************************************************
  常量宏定义 -- 设定全迷宫的终点坐标
*********************************************************************************************************/
#if MAZETYPE == 16
#define XDST0           7
#define XDST1           8
#define YDST0           7
#define YDST1           8
#endif


/*********************************************************************************************************
  常量宏定义 -- 设置迷宫搜索法则
  可以使用的法则:
  RIGHTMETHOD           -- 右手法则
  LEFTMETHOD            -- 左手法则
  CENTRALMETHOD         -- 中心法则
  FRONTRIGHTMETHOD      -- 中右法则
  FRONTLEFTMETHOD       -- 中左法则
  CORNERMETHOD          -- 向角法则
*********************************************************************************************************/
#define SEARCHMETHOD  RIGHTMETHOD                         /*  设置迷宫搜索法则为中心*/

/*********************************************************************************************************
  常量宏定义--前进一个迷宫格步进电机需要走的步数
*********************************************************************************************************/
#define	WHEEL_DIAMETER	1.8
#define PULSE_PER_CM		(0x4000/(WHEEL_DIAMETER*3.14159265))
#define ONEBLOCK        (PULSE_PER_CM*18)                              /*  电机走一格迷宫所需脉冲数  */


#endif
